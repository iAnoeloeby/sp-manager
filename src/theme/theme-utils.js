        "--input": "oklch(92.2% 0 0)",
        "--ring": "oklch(70.8% 0 0)",
        "--chart-1": "oklch(64.6% .222 41.116)",
        "--chart-2": "oklch(60% .118 184.704)",
        "--chart-3": "oklch(39.8% .07 227.392)",
        "--chart-4": "oklch(82.8% .189 84.429)",
        "--chart-5": "oklch(76.9% .188 70.08)",
